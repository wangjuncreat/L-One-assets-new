export const getCategoryName = (categoryId: string): string => {
  const category = categories.find(c => c.id === categoryId);
  return category?.name || categoryId;
};

export const getAssetsByCategory = (categoryId: string): Asset[] => {
  return assets.filter(asset => asset.category === categoryId);
};

export const getAllMovies = (): string[] => {
  const movies = new Set(assets.map(asset => asset.movie));
  return Array.from(movies);
};
